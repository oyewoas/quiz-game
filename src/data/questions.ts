// Helper function to get all questions for a specific category and difficulty
export const getQuestions = (
  category: string,
  difficulty: string
): Question[] => {
  return questionsByCategory[category]?.[difficulty] || [];
};

// Helper function to get all questions (flattened array)
export const getAllQuestions = (): Question[] => {
  return Object.values(questionsByCategory).flatMap((category) =>
    Object.values(category).flatMap((questions) => questions)
  );
};
